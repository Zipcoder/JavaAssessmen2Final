public class ArrayUtility<T> {

    private T value;

    public ArrayUtility(T value) {
        this.value = value;
    }

    public T getValue() {
        return value;
    }

    public ArrayUtility<Integer> Integer;
    public ArrayUtility<Long> Long;
    public ArrayUtility<String> String;
    public ArrayUtility<Object> Object;
}
